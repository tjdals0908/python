
# 문자열 다루기

txt = 'python is easy!'
print(txt[0])

print(txt[-1])  # 마지막인덱스  -1 -2 순서대로

print(txt[7:9]) #start index : stop index  - stop은 마지막 포함이 안된다

print(txt[0:2])

print(txt[2]) #0은 생략가능

print(txt[10:14])

print(txt[-5:-1])   # 마지막 데이터빼고 스탑 지정  , -로 끝에서 자릿수

print(txt[-5:])  #-5부터 끝까지


txt1= '012345678901234567890123456'

print(txt1[0:10])

